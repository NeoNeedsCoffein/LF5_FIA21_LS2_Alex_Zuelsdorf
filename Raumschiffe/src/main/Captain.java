package main;

public class Captain {
	
	private String name;
	private int steers;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSteers() {
		return steers;
	}
	public void setSteers(int steers) {
		this.steers = steers;
	}
	
}
